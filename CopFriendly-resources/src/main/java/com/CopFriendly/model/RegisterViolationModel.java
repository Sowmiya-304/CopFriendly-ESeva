package com.CopFriendly.model;

public class RegisterViolationModel {
	private String violators_name;
	private String driving_license;
	private String violation_type;
	private String vehicle_type;
	private String registration_number;
	private String phone_number;
	private String color;
	private String violation_date;
	private String violation_time;
	private String location;
	public String getViolators_name() {
		return violators_name;
	}
	public void setViolators_name(String violators_name) {
		this.violators_name = violators_name;
	}
	public String getDriving_license() {
		return driving_license;
	}
	public void setDriving_license(String driving_license) {
		this.driving_license = driving_license;
	}
	public String getViolation_type() {
		return violation_type;
	}
	public void setViolation_type(String violation_type) {
		this.violation_type = violation_type;
	}
	public String getVehicle_type() {
		return vehicle_type;
	}
	public void setVehicle_type(String vehicle_type) {
		this.vehicle_type = vehicle_type;
	}
	public String getRegistration_number() {
		return registration_number;
	}
	public void setRegistration_number(String registration_number) {
		this.registration_number = registration_number;
	}
	
	public String getPhone_number() {
		return phone_number;
	}
	public void setPhone_number(String phone_number) {
		this.phone_number = phone_number;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getViolation_date() {
		return violation_date;
	}
	public void setViolation_date(String violation_date) {
		this.violation_date = violation_date;
	}
	public String getViolation_time() {
		return violation_time;
	}
	public void setViolation_time(String violation_time) {
		this.violation_time = violation_time;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}	
}
